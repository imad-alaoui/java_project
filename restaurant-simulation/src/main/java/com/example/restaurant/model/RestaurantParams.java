package com.example.restaurant.model;

public class RestaurantParams {
    private int seats;
    private int openingHours;
    private int servingRate;

    // Getters and setters
    public int getSeats() { return seats; }
    public void setSeats(int seats) { this.seats = seats; }
    public int getOpeningHours() { return openingHours; }
    public void setOpeningHours(int openingHours) { this.openingHours = openingHours; }
    public int getServingRate() { return servingRate; }
    public void setServingRate(int servingRate) { this.servingRate = servingRate; }
}
